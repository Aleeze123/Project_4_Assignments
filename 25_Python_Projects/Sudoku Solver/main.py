import numpy as np
from termcolor import colored

class SudokuSolver:
    def __init__(self, board):
        self.board = np.array(board)

    def print_board(self):
        for i in range(9):
            row = ""
            for j in range(9):
                if self.board[i][j] == 0:
                    row += colored('0', 'yellow') + " "
                else:
                    row += colored(str(self.board[i][j]), 'blue') + " "
            print(row)

    def is_valid(self, row, col, num):
        for i in range(9):
            if self.board[row][i] == num or self.board[i][col] == num:
                return False
        start_row, start_col = 3 * (row // 3), 3 * (col // 3)
        for i in range(start_row, start_row + 3):
            for j in range(start_col, start_col + 3):
                if self.board[i][j] == num:
                    return False
        return True

    def solve(self):
        for row in range(9):
            for col in range(9):
                if self.board[row][col] == 0:
                    for num in range(1, 10):
                        if self.is_valid(row, col, num):
                            self.board[row][col] = num
                            if self.solve():
                                return True
                            self.board[row][col] = 0
                    return False
        return True

if __name__ == "__main__":
    board = [
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9]
    ]
    
    solver = SudokuSolver(board)
    print(colored("Initial Sudoku Board:", 'cyan'))
    solver.print_board()
    
    if solver.solve():
        print(colored("\nSolved Sudoku Board:", 'green'))
        solver.print_board()
    else:
        print(colored("\nNo solution exists.", 'red'))
